const mongoose = require("mongoose");

// Schema
const ownerSchema = mongoose.Schema({
  fullName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
    unique: true,
  },
  password: {
    type: String,
    required: true,
  },
});

// Model
module.exports = mongoose.model("owner", ownerSchema);
